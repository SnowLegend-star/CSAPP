int isAsciiDigit(int x) {
  //判断第四五位都是1、且高位都是0,1-3位值得小于等于9
  //x做完移位运算后自身值不发生改变的
  int a=x+~(0x30)+1;
  int b=0x39+~x+1;
  int c=a>>31;
  int d=b>>31;
  return !c&!d;
}
